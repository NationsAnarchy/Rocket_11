package com.vti.academy.entity;

public class MyMath {

    public static int findMax(int a, int b) {
        return a > b ? a : b;
    }

    public static int findMin(int a, int b) {
        return a > b ? b : a;
    }

    public static int calculateSum(int a, int b) {
        return a + b;
    }
}
